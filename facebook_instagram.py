#!/usr/bin/env python3
"""
Facebook + Instagram Integration

Social media posting automation with Mock Mode support.
All posts require HITL approval before publishing.

Usage:
    python3 facebook_instagram.py --post-fb "Your message"   # Post to Facebook
    python3 facebook_instagram.py --post-ig "Your message"   # Post to Instagram
    python3 facebook_instagram.py --summary                   # Weekly summary
    python3 facebook_instagram.py --demo                      # Run demo
    python3 facebook_instagram.py --history fb                # View FB history
    python3 facebook_instagram.py --history ig                # View IG history
    python3 facebook_instagram.py --status                    # Check status

Mock Mode (default):
    - Simulates posting without real API calls
    - Full HITL approval workflow
    - Posts saved to /Social_Media/ folder
"""

import os
import sys
import json
import logging
import hashlib
import shutil
from pathlib import Path
from datetime import datetime, timedelta
from typing import Dict, Any, List, Optional

# === Configuration ===
BASE_DIR = Path(__file__).parent
SOCIAL_MEDIA_DIR = BASE_DIR / "Social_Media"
PENDING_APPROVAL_DIR = BASE_DIR / "Pending_Approval"
APPROVED_DIR = BASE_DIR / "Approved"
REJECTED_DIR = BASE_DIR / "Rejected"
LOGS_DIR = BASE_DIR / "Logs"

FACEBOOK_POSTS_FILE = SOCIAL_MEDIA_DIR / "facebook_posts.md"
INSTAGRAM_POSTS_FILE = SOCIAL_MEDIA_DIR / "instagram_posts.md"
SOCIAL_MEDIA_LOG = LOGS_DIR / "social_media.log"

# Mock Mode - set to False when real API is configured
MOCK_MODE = os.environ.get("SOCIAL_MEDIA_MOCK_MODE", "true").lower() == "true"

# === Logging Setup ===
LOGS_DIR.mkdir(exist_ok=True)
SOCIAL_MEDIA_DIR.mkdir(exist_ok=True)

logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s | %(levelname)s | %(message)s',
    handlers=[
        logging.FileHandler(SOCIAL_MEDIA_LOG),
        logging.StreamHandler(sys.stdout)
    ]
)
logger = logging.getLogger(__name__)


# === Utility Functions ===

def generate_post_id(platform: str) -> str:
    """Generate unique post ID."""
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    unique = hashlib.md5(f"{platform}{timestamp}{os.urandom(4).hex()}".encode()).hexdigest()[:8]
    prefix = "FB" if platform == "facebook" else "IG"
    return f"{prefix}_{timestamp}_{unique}"


def extract_hashtags(content: str) -> List[str]:
    """Extract hashtags from content."""
    words = content.split()
    return [word for word in words if word.startswith("#")]


def create_approval_request(
    platform: str,
    content: str,
    post_type: str = "text_post",
    hashtags: List[str] = None
) -> Path:
    """Create HITL approval request for social media post."""
    PENDING_APPROVAL_DIR.mkdir(exist_ok=True)

    timestamp = datetime.now()
    post_id = generate_post_id(platform)

    # Truncate content for filename
    content_slug = content[:30].replace(" ", "_").replace("/", "_")
    content_slug = "".join(c for c in content_slug if c.isalnum() or c == "_")

    filename = f"SOCIAL_MEDIA_POST_{platform.capitalize()}_{content_slug}_{timestamp.strftime('%Y%m%d_%H%M%S')}.json"

    approval_data = {
        "action_type": "SOCIAL_MEDIA_POST",
        "platform": platform,
        "post_type": post_type,
        "post_id": post_id,
        "content": content,
        "hashtags": hashtags or extract_hashtags(content),
        "scheduled_time": None,
        "mock_mode": MOCK_MODE,
        "requires_approval": True,
        "created_at": timestamp.isoformat() + "Z",
        "expires_at": (timestamp + timedelta(hours=24)).isoformat() + "Z",
        "description": f"{platform.capitalize()} post: {content[:50]}..."
    }

    filepath = PENDING_APPROVAL_DIR / filename
    filepath.write_text(json.dumps(approval_data, indent=2))

    logger.info(f"Approval request created: {filename}")
    return filepath


def save_post_to_history(
    platform: str,
    post_id: str,
    content: str,
    hashtags: List[str] = None,
    status: str = "Published"
) -> None:
    """Save published post to history file."""
    SOCIAL_MEDIA_DIR.mkdir(exist_ok=True)

    history_file = FACEBOOK_POSTS_FILE if platform == "facebook" else INSTAGRAM_POSTS_FILE
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    # Create header if file doesn't exist
    if not history_file.exists():
        header = f"# {platform.capitalize()} Post History\n\n"
        header += f"*Generated by AI Employee Social Media Integration*\n\n"
        header += "---\n\n"
        history_file.write_text(header)

    # Append post entry
    mode_indicator = " (Mock Mode)" if MOCK_MODE else ""
    hashtag_str = " ".join(hashtags) if hashtags else "None"

    entry = f"""## {timestamp}

**Post ID:** {post_id}
**Status:** {status}{mode_indicator}
**Content:**
{content}

**Hashtags:** {hashtag_str}
**Engagement:** 0 likes, 0 comments{", 0 shares" if platform == "facebook" else ""} ({"simulated" if MOCK_MODE else "pending"})

---

"""

    with open(history_file, "a") as f:
        f.write(entry)

    logger.info(f"Post saved to history: {post_id}")


def mock_publish_post(platform: str, post_id: str, content: str, hashtags: List[str] = None) -> Dict[str, Any]:
    """Simulate publishing a post (Mock Mode)."""
    logger.info(f"[MOCK] Publishing to {platform}: {post_id}")

    # Simulate API response
    response = {
        "success": True,
        "post_id": post_id,
        "platform": platform,
        "mock_mode": True,
        "timestamp": datetime.now().isoformat(),
        "message": f"Post simulated successfully on {platform}"
    }

    # Save to history
    save_post_to_history(platform, post_id, content, hashtags, "Published")

    return response


# === Main Functions ===

def post_to_facebook(content: str, auto_approve: bool = False) -> Dict[str, Any]:
    """
    Create a Facebook post request.

    Args:
        content: The post content
        auto_approve: If True, automatically approve (for demo only)

    Returns:
        Dict with status and details
    """
    logger.info(f"Creating Facebook post request: {content[:50]}...")

    hashtags = extract_hashtags(content)

    # Create approval request
    approval_path = create_approval_request("facebook", content, "text_post", hashtags)

    result = {
        "status": "pending_approval",
        "platform": "facebook",
        "approval_file": str(approval_path),
        "content": content,
        "hashtags": hashtags,
        "mock_mode": MOCK_MODE
    }

    if auto_approve:
        # Move to approved and execute
        approved_path = APPROVED_DIR / approval_path.name
        APPROVED_DIR.mkdir(exist_ok=True)
        shutil.move(str(approval_path), str(approved_path))

        # Load and publish
        data = json.loads(approved_path.read_text())
        publish_result = mock_publish_post(
            "facebook",
            data["post_id"],
            data["content"],
            data["hashtags"]
        )

        result["status"] = "published"
        result["publish_result"] = publish_result

    return result


