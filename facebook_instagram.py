def get_weekly_summary() -> Dict[str, Any]:
    """Generate weekly summary of social media posts."""
    summary = {
        "period": {
            "start": (datetime.now() - timedelta(days=7)).strftime("%Y-%m-%d"),
            "end": datetime.now().strftime("%Y-%m-%d")
        },
        "facebook": {
            "posts": 0,
            "engagement": {"likes": 0, "comments": 0, "shares": 0}
        },
        "instagram": {
            "posts": 0,
            "engagement": {"likes": 0, "comments": 0}
        },
        "pending_approvals": 0,
        "rejected": 0
    }

    # Count Facebook posts
    if FACEBOOK_POSTS_FILE.exists():
        content = FACEBOOK_POSTS_FILE.read_text()
        summary["facebook"]["posts"] = content.count("**Post ID:** FB_")

    # Count Instagram posts
    if INSTAGRAM_POSTS_FILE.exists():
        content = INSTAGRAM_POSTS_FILE.read_text()
        summary["instagram"]["posts"] = content.count("**Post ID:** IG_")

    # Count pending approvals
    if PENDING_APPROVAL_DIR.exists():
        for f in PENDING_APPROVAL_DIR.glob("SOCIAL_MEDIA_POST_*.json"):
            summary["pending_approvals"] += 1

    # Count rejected
    if REJECTED_DIR.exists():
        for f in REJECTED_DIR.glob("SOCIAL_MEDIA_POST_*.json"):
            summary["rejected"] += 1

    return summary


