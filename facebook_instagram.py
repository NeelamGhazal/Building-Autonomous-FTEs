def show_history(platform: str):
    """Display post history for a platform."""
    if platform == "fb" or platform == "facebook":
        history_file = FACEBOOK_POSTS_FILE
        platform_name = "Facebook"
    elif platform == "ig" or platform == "instagram":
        history_file = INSTAGRAM_POSTS_FILE
        platform_name = "Instagram"
    else:
        print(f"Unknown platform: {platform}")
        print("Use 'fb' for Facebook or 'ig' for Instagram")
        return

    print(f"\n{'='*60}")
    print(f"{platform_name.upper()} POST HISTORY")
    print("="*60 + "\n")

    if not history_file.exists():
        print(f"No {platform_name} posts yet.")
        return

    print(history_file.read_text())


def show_status():
    """Show current status of social media integration."""
    print("\n" + "="*60)
    print("SOCIAL MEDIA INTEGRATION STATUS")
    print("="*60)

    # Mode
    print(f"\nMode: {'MOCK (simulated)' if MOCK_MODE else 'PRODUCTION'}")

    # Files
    print("\nHistory Files:")
    if FACEBOOK_POSTS_FILE.exists():
        fb_count = FACEBOOK_POSTS_FILE.read_text().count("**Post ID:** FB_")
        print(f"  Facebook: {fb_count} posts")
    else:
        print("  Facebook: No posts yet")

    if INSTAGRAM_POSTS_FILE.exists():
        ig_count = INSTAGRAM_POSTS_FILE.read_text().count("**Post ID:** IG_")
        print(f"  Instagram: {ig_count} posts")
    else:
        print("  Instagram: No posts yet")

    # Pending
    print("\nPending Approvals:")
    if PENDING_APPROVAL_DIR.exists():
        pending = list(PENDING_APPROVAL_DIR.glob("SOCIAL_MEDIA_POST_*.json"))
        if pending:
            for f in pending[:5]:
                data = json.loads(f.read_text())
                print(f"  - {data['platform']}: {data['content'][:40]}...")
            if len(pending) > 5:
                print(f"  ... and {len(pending) - 5} more")
        else:
            print("  None")
    else:
        print("  None")

    print("\n" + "="*60)


