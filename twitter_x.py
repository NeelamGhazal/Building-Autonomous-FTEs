        if arg == "--tweet" and len(sys.argv) > 2:
            content = sys.argv[2]
            result = post_tweet(content)
            length = result["character_count"]
            limit = result["character_limit"]

            print(f"\nTweet request created!")
            print(f"Characters: {length}/{limit}")
            if result["exceeds_limit"]:
                print(f"WARNING: Exceeds limit by {length - limit} characters!")
            print(f"Status: {result['status']}")
            print(f"Approval file: {Path(result['approval_file']).name}")
            print(f"\nTo approve: Move the file to /Approved/")
            print(f"To reject: Move the file to /Rejected/")

        elif arg == "--summary":
            show_summary()

        elif arg == "--history":
            show_history()

        elif arg == "--status":
            show_status()

        elif arg == "--demo":
            run_demo()

        elif arg == "--help":
            print(__doc__)

        else:
            print(f"Unknown option: {arg}")
            print(__doc__)

